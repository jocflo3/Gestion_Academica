CREATE TABLE MATERIA_DISPONIBLE
(
	ID_HORARIO NVARCHAR(8) PRIMARY KEY,
	ID_MATERIA NVARCHAR(8) NULL,
	ID_ACT NVARCHAR(8) NULL,
	ID_PROFE  NVARCHAR(8) NOT NULL,
	FH_INI DATE NOT NULL,
	FH_FIN DATE NOT NULL,
	ID_ESTADO INT NOT NULL DEFAULT 1,
	PERIODO INT NOT NULL,
	CUPO INT NOT NULL,
	FH_ALTA DATETIME2 NOT NULL DEFAULT GETDATE(),
	FH_MOD DATETIME2 NULL,
	CONSTRAINT FK_MDISPONIBLES_ESTADO FOREIGN KEY(ID_ESTADO) REFERENCES ESTADO(ID_ESTADO),
	CONSTRAINT FK_MDISPONIBLES_PROFE FOREIGN KEY(ID_PROFE) REFERENCES PROFESOR(ID_PROFE),
	CONSTRAINT FK_MDISPONIBLES_AEXT FOREIGN KEY(ID_ACT) REFERENCES ACT_EXTRACURRUCULAR(ID_ACT),
	CONSTRAINT FK_MDISPONIBLES_PERIODO FOREIGN KEY(PERIODO) REFERENCES PERIODO(ID_PERIODO),
	CONSTRAINT FK_MDISPONIBLES_MATERIA FOREIGN KEY(ID_MATERIA) REFERENCES MATERIA(ID_MATERIA),
	CONSTRAINT CK_MATERIA_O_ACT CHECK ((ID_MATERIA IS NOT NULL AND ID_ACT IS NULL) OR (ID_MATERIA IS NULL AND ID_ACT IS NOT NULL))
);
