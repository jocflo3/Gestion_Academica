CREATE TABLE PERIODO
(
	ID_PERIODO INT PRIMARY KEY IDENTITY(1,1),
	DES_PERIODO NVARCHAR(250) NOT NULL,
	FH_ALTA DATETIME2 NOT NULL DEFAULT GETDATE()
);
