CREATE TABLE HORARIO_DETALLE
(
    ID INT IDENTITY PRIMARY KEY,
    ID_HORARIO NVARCHAR(8) NOT NULL,
    DIA INT NOT NULL,
    HORA_INI TIME NOT NULL,
    HORA_FIN TIME NOT NULL,
    CONSTRAINT FK_HD_HORARIO FOREIGN KEY(ID_HORARIO) 
        REFERENCES MATERIA_DISPONIBLE(ID_HORARIO)
);

CREATE TABLE INSCRIPCION
(
    CONSE INT PRIMARY KEY IDENTITY(1,1),
    ID_ALUMNO NVARCHAR(8) NOT NULL,
    ID_HORARIO NVARCHAR(8) NOT NULL,
    FH_ALTA DATETIME2 NOT NULL DEFAULT GETDATE(),
    FH_MOD DATETIME2 NULL,
    CALIFICACION DECIMAL(5,2) NULL,
    OPORTUNIDAD INT NOT NULL DEFAULT 1,
    ACREDITADA BIT NULL,
	CONSTRAINT UQ_ALUMNO_HORARIO UNIQUE(ID_ALUMNO, ID_HORARIO),
    CONSTRAINT FK_INSCRIPCION_ALUMNO FOREIGN KEY(ID_ALUMNO) REFERENCES ALUMNO(NO_CONTROL),
    CONSTRAINT FK_INSCRIPCION_HORARIO FOREIGN KEY(ID_HORARIO) REFERENCES MATERIA_DISPONIBLE(ID_HORARIO),
	CONSTRAINT CK_CALIFICACION CHECK (CALIFICACION BETWEEN 0 AND 100)
);

CREATE TABLE USUARIO
(
    ID_USUARIO INT PRIMARY KEY IDENTITY(1,1),
    USERNAME NVARCHAR(50) NOT NULL UNIQUE,
    PASSWORD_HASH NVARCHAR(255) NOT NULL,
    NO_CONTROL NVARCHAR(8) NULL,
    ID_PROFE NVARCHAR(8) NULL,
    REFRESH_TOKEN NVARCHAR(MAX) NULL,
    REFRESH_TOKEN_EXP DATETIME2 NULL,
    ID_ESTADO INT NOT NULL DEFAULT 1,
    FH_ALTA DATETIME2 NOT NULL DEFAULT GETDATE(),
    FH_MOD DATETIME2 NULL,
	ACTIVO BIT NOT NULL DEFAULT 0,
    CONSTRAINT FK_USUARIO_ALUMNO FOREIGN KEY(NO_CONTROL) REFERENCES ALUMNO(NO_CONTROL),
    CONSTRAINT FK_USUARIO_PROFE FOREIGN KEY(ID_PROFE) REFERENCES PROFESOR(ID_PROFE),
    CONSTRAINT FK_USUARIO_ESTADO FOREIGN KEY(ID_ESTADO) REFERENCES ESTADO(ID_ESTADO)
);

CREATE TABLE ROL
(
    ID_ROL INT PRIMARY KEY IDENTITY(1,1),
    DESC_ROL NVARCHAR(50) NOT NULL
);

CREATE TABLE USUARIO_ROL
(
    ID_USUARIO INT NOT NULL,
    ID_ROL INT NOT NULL,
    CONSTRAINT FK_UR_USUARIO FOREIGN KEY(ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT FK_UR_ROL FOREIGN KEY(ID_ROL) REFERENCES ROL(ID_ROL),
	CONSTRAINT PK_USUARIO_ROL PRIMARY KEY(ID_USUARIO, ID_ROL)
);


